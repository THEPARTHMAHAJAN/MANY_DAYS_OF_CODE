#include <iostream>
using namespace std;

int main()
{
    char ch;
    cin>>ch;
    int t;
    cin>>t;
    while(t--)
    {
        int n;
        cin>>n;
        for(int i=0;i<n;i++)
        {
            cout<<ch;
        }
        cout<<endl;
    }
    return 0;
}
